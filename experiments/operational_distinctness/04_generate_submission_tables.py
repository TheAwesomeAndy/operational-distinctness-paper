def escape_latex(s: str) -> str:
    repl = {
        "\\": r"\\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    out = []
    for ch in str(s):
        out.append(repl.get(ch, ch))
    return "".join(out)


def _write_table(path: Path, lines: list[str]) -> None:
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


# ─── Table 1: feature blocks ──────────────────────────────────────────

def table1_feature_blocks() -> Path:
    path = cfg.TABLE_DIR / "table1_feature_blocks.tex"
    lines = [
        r"% Auto-generated by 04_generate_submission_tables.py",
        r"\begin{tabular}{llrp{0.32\linewidth}p{0.22\linewidth}}",
        r"\hline",
        r"Block & Source & Dimension & Description & Operational role \\",
        r"\hline",
    ]
    for r in FEATURE_BLOCK_TABLE:
        lines.append(
            f"{escape_latex(r['block'])} & {escape_latex(r['source'])} & "
            f"{r['dimension']} & {escape_latex(r['description'])} & "
            f"{escape_latex(r['operational_role'])} \\\\"
        )
    lines += [r"\hline", r"\end{tabular}"]
    _write_table(path, lines)
    return path


# ─── Table 2: affective ablation ──────────────────────────────────────

def table2_affective_ablation() -> Path | None:
    src = cfg.OUT_DIR / "affective_ablation_metrics.csv"
    if not src.exists():
        print(f"  [table2] Skipped: {src} not found.")
        return None
    df = pd.read_csv(src).set_index("config_id").loc[cfg.AFFECTIVE_CONFIG_ORDER].reset_index()

    path = cfg.TABLE_DIR / "table2_affective_ablation.tex"
    lines = [
        r"% Auto-generated by 04_generate_submission_tables.py",
        r"\begin{tabular}{llrrrr}",
        r"\hline",
        r"Config & Feature set & Dim & Bal. acc. (\%) & Macro-F1 & Macro ROC-AUC \\",
        r"\hline",
    ]
    for _, r in df.iterrows():
        bal_mean = r["balanced_accuracy_mean"] * 100
        bal_sd = r["balanced_accuracy_sd"] * 100
        f1_mean = r["macro_f1_mean"]
        f1_sd = r["macro_f1_sd"]
        auc_mean = r["macro_roc_auc_ovr_mean"]
        auc_sd = r["macro_roc_auc_ovr_sd"]
        lines.append(
            f"{escape_latex(r['config_id'])} & {escape_latex(r['feature_set'])} & "
            f"{int(r['dimension'])} & "
            f"${bal_mean:.2f} \\pm {bal_sd:.2f}$ & "
            f"${f1_mean:.3f} \\pm {f1_sd:.3f}$ & "
            f"${auc_mean:.3f} \\pm {auc_sd:.3f}$ \\\\"
        )
    lines += [r"\hline", r"\end{tabular}"]
    _write_table(path, lines)
    return path


# ─── Table 3: best layer per diagnosis ────────────────────────────────

INTERPRETATION_RULES = {
    "C1": "embedding-space sensitivity",
    "C2": "weak dynamical sensitivity",
    "C3": "weak topological sensitivity",
    "C4": "weak topological/dynamical sensitivity",
    "C5": "broad multi-layer sensitivity",
    "C6": "weak coupling-layer sensitivity",
}

NULL_INTERPRETATION = "no meaningful layer separation"


def _interpretation(best_cfg: str, best_bal_acc: float) -> str:
    if best_bal_acc < 0.55:
        return NULL_INTERPRETATION
    return INTERPRETATION_RULES.get(best_cfg, "weak layer-specific sensitivity")


def table3_clinical_sensitivity() -> Path | None:
    src = cfg.OUT_DIR / "clinical_sensitivity_metrics.csv"
    if not src.exists():
        print(f"  [table3] Skipped: {src} not found.")
        return None
    df = pd.read_csv(src)
    if df.empty:
        return None

    path = cfg.TABLE_DIR / "table3_clinical_sensitivity.tex"
    lines = [
        r"% Auto-generated by 04_generate_submission_tables.py",
        r"\begin{tabular}{lllrrl}",
        r"\hline",
        r"Diagnosis & Best config & Feature set & Bal. acc. (\%) & ROC-AUC & Interpretation \\",
        r"\hline",
    ]
    for dx in cfg.DIAGNOSES:
        sub = df[df["diagnosis"] == dx]
        if sub.empty:
            continue
        # Rank by balanced accuracy; report ROC-AUC for the same row.
        best = sub.loc[sub["balanced_accuracy_mean"].idxmax()]
        bal_mean = best["balanced_accuracy_mean"] * 100
        auc_mean = best["roc_auc_mean"]
        lines.append(
            f"{escape_latex(dx)} & {escape_latex(best['config_id'])} & "
            f"{escape_latex(best['feature_set'])} & "
            f"{bal_mean:.2f} & {auc_mean:.3f} & "
            f"{escape_latex(_interpretation(best['config_id'], best['balanced_accuracy_mean']))} \\\\"
        )
    lines += [r"\hline", r"\end{tabular}"]
    _write_table(path, lines)
    return path


# ─── Table 4: full diagnosis x config matrix ──────────────────────────

def table4_full_clinical_matrix() -> Path | None:
    src = cfg.OUT_DIR / "clinical_sensitivity_metrics.csv"
    if not src.exists():
        print(f"  [table4] Skipped: {src} not found.")
        return None
    df = pd.read_csv(src)
    if df.empty:
        return None

    path = cfg.TABLE_DIR / "table4_full_clinical_matrix.tex"
    configs = cfg.CLINICAL_CONFIG_ORDER
    column_spec = "l" + "c" * len(configs)
    lines = [
        r"% Auto-generated by 04_generate_submission_tables.py",
        r"% Cell format: balanced accuracy (\%) / ROC-AUC",
        r"\begin{tabular}{" + column_spec + r"}",
        r"\hline",
        "Diagnosis & " + " & ".join(configs) + r" \\",
        r"\hline",
    ]
    for dx in cfg.DIAGNOSES:
        sub = df[df["diagnosis"] == dx]
        if sub.empty:
            continue
        cells = [escape_latex(dx)]
        for cid in configs:
            row = sub[sub["config_id"] == cid]
            if row.empty:
                cells.append("--")
                continue
            bal = row["balanced_accuracy_mean"].iloc[0] * 100
            auc = row["roc_auc_mean"].iloc[0]
            cells.append(f"{bal:.1f} / {auc:.3f}")
        lines.append(" & ".join(cells) + r" \\")
    lines += [r"\hline", r"\end{tabular}"]
    _write_table(path, lines)
    return path


def main() -> int:
    print("=" * 70)
    print("04: Generate submission tables")
    print("=" * 70)
    produced = []
    for fn in (table1_feature_blocks, table2_affective_ablation,
               table3_clinical_sensitivity, table4_full_clinical_matrix):
        out = fn()
        if out is not None:
            produced.append(out)
            print(f"  -> {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
